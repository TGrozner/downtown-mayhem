await mkdir(dirname(outputPath), { recursive: true });
if ((await readExistingSource(outputPath)) === source) {
  console.log(`building kit profiles unchanged -> ${outputPath}`);
} else {
  await writeFile(outputPath, source, "utf8");
  console.log(`building kit profiles generated -> ${outputPath}`);
}

async function readExistingSource(path) {
  try {
    return await readFile(path, "utf8");
  } catch {
    return null;
  }
}

function validateProfile(name, profile) {
  const requiredObjects = ["facade", "windows", "massing", "weathering", "silhouette", "storefront", "roof"];
  for (const key of requiredObjects) {
    if (!profile?.[key] || typeof profile[key] !== "object") {
      throw new Error(`Building kit profile ${name} is missing object ${key}`);
    }
  }
  for (const key of ["frontRibs", "horizontalBands", "sideRibs", "weathering.grime"]) {
    const value = key.includes(".") ? key.split(".").reduce((entry, segment) => entry?.[segment], profile) : profile[key];
    if (!Array.isArray(value)) {
      throw new Error(`Building kit profile ${name} is missing array ${key}`);
    }
  }
  assertFiniteProfileNumbers(name, profile);
  const { rows, columns, glass, frame } = profile.windows;
  if (!Number.isInteger(rows) || rows < 1 || rows > 4) {
    throw new Error(`Building kit profile ${name} has invalid window row count`);
  }
  if (!Number.isInteger(columns) || columns < 1 || columns > 4) {
    throw new Error(`Building kit profile ${name} has invalid window column count`);
  }
  if (!["cool_window", "warm_window", "shop_glass"].includes(glass)) {
    throw new Error(`Building kit profile ${name} has invalid window glass material ${String(glass)}`);
  }
  if (!["full", "standard", "lean"].includes(frame)) {
    throw new Error(`Building kit profile ${name} has invalid window frame mode ${String(frame)}`);
  }
}

function assertFiniteProfileNumbers(profileName, value, path = profileName) {
  if (Array.isArray(value)) {
    for (let index = 0; index < value.length; index += 1) {
      assertFiniteProfileNumbers(profileName, value[index], `${path}[${index}]`);
    }
    return;
  }
  if (!value || typeof value !== "object") {
    return;
  }
  for (const [key, entry] of Object.entries(value)) {
    if (typeof entry === "number" && !Number.isFinite(entry)) {
      throw new Error(`Building kit profile ${profileName} has invalid number at ${path}.${key}`);
    }
    assertFiniteProfileNumbers(profileName, entry, `${path}.${key}`);
  }
}
